#while 语法结构
"""
while 条件表达式:
    循环体语句 1
    循环体语句 2
    ....
"""
print("案例1:")
#案例-------打印十遍"人生苦短,我用Python"
i = 0
while i < 10:
    print(f"第{i+1}遍: 人生苦短,我用Python")
    i += 1
print("循环结束")

print("\n-----------------------------------------\n")

print("案例2:")
#案例--------计算1--100之间所有偶数的累加之和
j = 2
sum = 0
while j < 101 :
    sum += j
    j += 2
print (f"1--100之间所有偶数之和为{sum}")
